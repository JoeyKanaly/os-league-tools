const getById = id => {
  const questName = questNamesById.get(id);
  if (!questName) {
    throw new Error(`questNameById not found ${id}`);
  }
  const questByName = QUESTS.find(quest => quest.label === questName);
  if (!questByName) {
    throw new Error(`questByName not found ${id} '${questName}'`);
  }
  const questDetails = questByName;
  questDetails.id = id;
  if (!questDetails) {
    throw new Error(`quest not found ${id}`);
  }
  return questDetails;
};

